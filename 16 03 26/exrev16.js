//Criar um programa com uma condicional composta para verificar a temperatura entre "frio" "agradavel" ou "quente"
let temperatura = 22;

if (temperatura < 15) {
    console.log("Frio");
} else if (temperatura >= 15 && temperatura <= 25) {
    console.log("Agradável");
} else {
    console.log("Quente");
}
